class GuideGenerator:
    """플래너의 JSON 결과를 사람이 읽기 쉬운 가이드 문서로 변환합니다."""
    
    @staticmethod
    def generate_markdown(body_name, strategy, plans):
        lines = []
        lines.append(f"# Decomposition Guide: {body_name}")
        lines.append(f"**Overall Strategy:** {strategy}")
        lines.append("\n## Step-by-Step Instructions")
        
        if not plans:
            lines.append("*No specific steps were generated for this body.*")
            return "\n".join(lines)
            
        for i, plan in enumerate(plans):
            # 전략 이름 정규화
            raw_strat = str(plan.get('strategy', 'UNKNOWN')).upper()
            lines.append(f"### [Step {i+1}] {raw_strat} Operation")
            
            # 1. O-GRID (원통형 코어 분할)
            if "OGRID" in raw_strat:
                offset = plan.get('core_offset', 0.0)
                # [v4.81] 이미 mm인 경우 추가 곱셈 제거
                display_val = f"{offset:.2f}mm"
                
                lines.append(f"- **Action:** Create a circular O-grid core (offset: **{display_val}**).")
                lines.append("- **Purpose:** Ensures high-quality hexa-mesh in the center of cylinders.")
            
            # 2. AXIAL (축방향 단차 분할)
            elif "AXIAL" in raw_strat:
                display_pos = plan.get('display_pos')
                if not display_pos:
                    origin = plan.get('split_plane', {}).get('origin', [0,0,0])
                    z_val = origin[2] if len(origin) > 2 else 0.0
                    display_pos = f"{z_val:.2f}mm"

                lines.append(f"- **Action:** Split the body horizontally at **Z = {display_pos}**.")
                lines.append("- **Purpose:** Isolate features or handle step changes in diameter.")

            # 3. SECTOR (90도 방사형 분할)
            elif "SECTOR" in raw_strat:
                lines.append("- **Action:** Perform a 90-degree radial cross-split.")
                lines.append("- **Purpose:** Create manageable quadrants for standard meshing.")

            # 4. HGRID (박스형 평면 분할)
            elif "HGRID" in raw_strat:
                lines.append("- **Action:** Apply orthogonal planar splits to the block-based body.")
                lines.append("- **Purpose:** Maintain grid alignment for rectangular features or create matrices for perforated plates.")

            # 5. TRANSVERSE / JUNCTION / YBLOCK (교차로/굽힘 분할)
            elif "TRANSVERSE" in raw_strat or "JUNCTION" in raw_strat or "YBLOCK" in raw_strat:
                lines.append("- **Action:** Perform transverse/Y-block cuts at the pipe junction or elbow.")
                lines.append("- **Purpose:** Isolate the complex intersection zone for structured meshing.")

            # 6. CGRID (벽면 근접 구멍)
            elif "CGRID" in raw_strat:
                lines.append("- **Action:** Create a semi-circular C-grid around the hole.")
                lines.append("- **Purpose:** Prevent element distortion when a hole is too close to a wall (Low WHR).")

            # 7. RADIAL_OFFSET (두꺼운 원통)
            elif "RADIAL_OFFSET" in raw_strat:
                lines.append("- **Action:** Create a concentric cylindrical split (Radial Offset).")
                lines.append("- **Purpose:** Divide a thick-walled cylinder to maintain aspect ratio.")

            # 8. Fallback (기타 정의되지 않은 모든 경우)
            else:
                lines.append(f"- **Action:** Execute specialized decomposition ({raw_strat}).")
                lines.append("- **Purpose:** Simplify geometry for structured meshing.")
            
            lines.append("")
            
        lines.append("\n---")
        lines.append("*Generated by AntiGravity Solid Decomposer (v3.1)*")
        
        return "\n".join(lines)
