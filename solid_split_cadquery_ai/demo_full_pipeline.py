def create_mid_disk_cylinder():
    """테스트용 복합 형상 생성: 실린더 중간에 원판이 있는 형상"""
    cyl = cq.Workplane("XY").cylinder(100, 10)
    disk = cq.Workplane("XY").circle(30).extrude(10).translate((0,0,-5))
    return cyl.union(disk)

def export_svg_views(model, prefix, output_dir):
    """모델의 다중 뷰(Top, Side, Iso)를 SVG로 내보냅니다."""
    views = {
        "top": (0, 0, 1),
        "side": (1, 0, 0),
        "iso": (1, 1, 1)
    }
    paths = {}
    for name, direction in views.items():
        filename = f"{prefix}_{name}.svg"
        path = os.path.join(output_dir, filename)
        cq.exporters.export(model, path, cq.exporters.ExportTypes.SVG, 
                           opt={
                               "width": 400, "height": 300, 
                               "marginLeft": 5, "marginTop": 5, 
                               "showAxes": False,
                               "projectionDir": direction,
                               "strokeWidth": 0.5,
                               "strokeColor": (56, 189, 248) if prefix == "after" else (148, 163, 184)
                           })
        paths[name] = filename
    return paths

