def generate_report_md(output_dir, plan, summary):
    """AI의 판단 근거를 포함한 상세 Markdown 리포트를 생성합니다."""
    md_content = f"""# Solid Decomposition AI Report

## 1. 모델 요약 (Model Overview)
- **전체 크기 (Bounding Box)**: {summary['overall_size']}
- **감지된 주요 피처 수**: {len(summary['features'])}

## 2. 기하학적 특징 분석 (Feature Extraction)
AI가 형상에서 추출한 주요 데이터입니다:
"""
    for i, f in enumerate(summary['features']):
        md_content += f"- **피처 {i+1}**: Type: `{f['type']}`, Location: `{f.get('location', 'N/A')}`\n"

    md_content += f"""
## 3. AI 분할 전략 (AI Strategy & Reasoning)
AI(`Gemini-SolidEngine`)가 수립한 최적의 분할 계획입니다.

### 전략명: {plan['strategy_description']}
**판단 근거 (Reasoning)**:
{plan.get('reasoning', '형상의 불연속성(Junction)과 동축성을 고려하여 메쉬 품질을 높일 수 있는 평면 절단을 제안합니다.')}

## 4. 상세 분할 명령어 (Splitting Operations)
| 순서 | 작업 유형 | 축 | 좌표 | 이유 |
| :--- | :--- | :--- | :--- | :--- |
"""
    for i, s in enumerate(plan['splits']):
        md_content += f"| {i+1} | {s['operation']} | {s.get('axis', '-')} | {s.get('coordinate', '-')} | {s.get('reason', '-')} |\n"

    md_content += """
---
*본 리포트는 AI에 의해 자동 생성되었습니다.*
"""
    with open(os.path.join(output_dir, "decomposition_report.md"), "w", encoding="utf-8") as f:
        f.write(md_content)

def main():
    print("🚀 Starting Full AI Pipeline (Multi-View Mode)...")
    
    # 1. 형상 생성
    model = create_mid_disk_cylinder()
    
    # 2. 형상 분석 (Extractor)
    extractor = GeometryExtractor(model)
    summary = extractor.extract_summary()
    
    # 3. AI 판단 (Gemini)
    ai_plan = {
        "strategy_description": "Disk Isolation Strategy",
        "reasoning": "중앙의 큰 원판은 위아래 실린더와 기하학적 접합부(Junction)를 형성합니다. 이 접합부 평면을 따라 자르면 3개의 단순한 스윕(Sweep) 가능한 형상으로 나뉘어 육면체 메쉬(Hex-mesh) 품질이 비약적으로 향상됩니다.",
        "splits": [
            {"operation": "plane_cut", "axis": "Z", "coordinate": -5.0, "reason": "Lower Disk Junction"},
            {"operation": "plane_cut", "axis": "Z", "coordinate": 5.0, "reason": "Upper Disk Junction"}
        ]
    }
    
    # 4. 결과 디렉토리 정리
    output_dir = "report"
    if os.path.exists(output_dir):
        shutil.rmtree(output_dir)
    os.makedirs(output_dir)

    # 5. 시각화 데이터 생성
    before_views = export_svg_views(model, "before", output_dir)
    
    # 6. 분할 실행 (Executor)
    executor = SplitExecutor(model)
    solids = executor.execute_plan(ai_plan)
    
    # 7. 시각화 데이터 생성 (After)
    combined_after = cq.Workplane("XY")
    for b in solids:
        combined_after = combined_after.add(b)
    after_views = export_svg_views(combined_after, "after", output_dir)
    
    # 8. 리포트 생성 (HTML & Markdown)
    generate_report_html(output_dir, ai_plan, summary, before_views, after_views)
    generate_report_md(output_dir, ai_plan, summary)
    
    print(f"✅ Pipeline Complete!")
    print(f"📊 HTML Report: {output_dir}/index.html")
    print(f"📝 Markdown Report: {output_dir}/decomposition_report.md")

if __name__ == "__main__":
    main()
